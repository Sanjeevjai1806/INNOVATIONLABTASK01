// Math Functions
console.log(Math.random());                  // Random number
console.log(Math.floor(4.7));               // Rounds down to 4
console.log(Math.ceil(4.3));                // Rounds up to 5
console.log(Math.max(3, 5, 1));             // 5
console.log(Math.min(3, 5, 1));             // 1

// String Functions
let name = "sanjeev";
console.log(name.toUpperCase());            // "SANJEEV"
console.log(name.slice(0, 3));              // "san"
console.log(name.includes("jeev"));          // true
console.log(name.replace("san", "shan"));    // "shanjeev"